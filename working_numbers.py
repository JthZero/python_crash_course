
#Integers
print(2+3)
print(2-3)
print(2*3)
print(2/3,'\n')  # Division


print(2**3)  # Exponentiation
print(2**3**2)  # Exponentiation with precedence
print(2**(3**2),'\n')  # Exponentiation with parentheses

print(2+3*4)
print((2+3)*4,'\n')  # Parentheses change precedence

# Floats    
print(0.1 + 0.1)
print(0.2 + 0.2)
print(2*0.1)
print(2*0.2,'\n')
print(0.1 + 0.2)  # Floating point arithmetic can be imprecise
print(3 * 0.1)  # This is also imprecise

# Integers and Floats
print(4/2)
print(1 + 2.0)
print(2 * 3.0)
print(3.0 ** 2, '\n')  # Mixing integers and floats

# Underscore in Numbers
print(1_000_000)  # Readable large numbers
print(1_000_000.0)  # Readable large floats
print(1_000_000 + 2_000_000)  # Operations with underscores
print(1_000_000 * 2)  # Operations with underscores
print(1_000_000 / 2,'\n')  # Operations with underscores

# Multiple Assignment
a, b, c = 1, 2, 3
print(a, b, c)  # Print multiple variables
print(a + b + c,'\n')  # Sum of multiple variables

# Constants
PI = 3.14159  # Example of a constant
print(PI)  # Print the constant
print(PI * 2)  # Use the constant in a calculation  